class PrimitiveDatatypes {
    static boolean def_bool;
    static double def_double;
    static float def_float;
    static int def_int;
    static long def_long;
    static String def_string;
    public static void main(String args[]){
        System.out.println("Default values.....");
        System.out.println("Default boolean value : " + def_bool);
        System.out.println("Default double value : " + def_double);
        System.out.println("Default float value : " + def_float);
        System.out.println("Default integer value : " + def_int);
        System.out.println("Default long value : " + def_long);
        System.out.println("Default String value : " + def_string);
    }
}
